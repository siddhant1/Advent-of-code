function getSelectAdder(opp, winCondition) {
    const isWin = winCondition === 6
    const isDraw = winCondition === 3
    const isLose = !isWin && !isDraw

    const rock = 1;
    const paper = 2
    const scissor = 3

    if (OppMap[opp] === "R" && isWin) {
        return paper
    }

    if (OppMap[opp] === "P" && isWin) {
        return scissor
    }

    if (OppMap[opp] === "S" && isWin) {
        return rock
    }

    //

    if (OppMap[opp] === "R" && isLose) {
        return scissor
    }

    if (OppMap[opp] === "P" && isLose) {
        return rock
    }

    if (OppMap[opp] === "S" && isLose) {
        return paper
    }

    //

    if (OppMap[opp] === "R" && isDraw) {
        return rock
    }

    if (OppMap[opp] === "P" && isDraw) {
        return paper
    }

    if (OppMap[opp] === "S" && isDraw) {
        return scissor
    }

}



